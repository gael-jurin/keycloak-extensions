package com.spvie.oauth.provider.event.logging;

import org.keycloak.Config;
import org.keycloak.events.EventListenerProvider;
import org.keycloak.events.EventListenerProviderFactory;
import org.keycloak.models.KeycloakSession;
import org.keycloak.models.KeycloakSessionFactory;

public class LogstashEventListenerProviderFactory implements EventListenerProviderFactory {
    public static final String PROVIDER_ID = "auth-event-listener";
    public static final LogstashEventListenerProvider SINGLETON = new LogstashEventListenerProvider();

    @Override
    public EventListenerProvider create(KeycloakSession session) {
        return SINGLETON;
    }

    @Override
    public void init(Config.Scope config) {
    }

    @Override
    public void postInit(KeycloakSessionFactory factory) {
    }

    @Override
    public void close() {
    }

    @Override
    public String getId() {
        return PROVIDER_ID;
    }
}
